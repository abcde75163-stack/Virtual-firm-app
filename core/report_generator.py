def _summarize_prev_sections(prev_text: str, max_chars: int) -> str:
    """이전 섹션 텍스트에서 표를 제거하고 핵심 텍스트만 요약 전달"""
    if not prev_text:
        return ""
    lines = prev_text.split("\n")
    filtered = []
    for line in lines:
        if line.strip().startswith("|"):
            continue
        if not line.strip() and filtered and not filtered[-1].strip():
            continue
        filtered.append(line)
    summary = "\n".join(filtered)
    if len(summary) > max_chars:
        summary = summary[:max_chars] + "\n\n[... 이전 내용 일부 생략 ...]"
    return summary


def generate_vf_report_streaming(
    api_key:        str,
    patent_text:    str,
    section_name:   str,
    prev_sections:  str,
    biz_type:       str = "자동 판단 (AI 추론)",
    demand_company: str = "",
    demand_text:    str = "",
    extra_note:     str = "",
) -> Generator[str, None, None]:
    """Claude API 스트리밍으로 섹션별 보고서 생성"""
    client = anthropic.Anthropic(api_key=api_key)

    patent_text_trimmed = truncate_text(patent_text, max_chars=25000)

    prev_max = PREV_SECTION_SUMMARY_CHARS.get(section_name, 0)
    prev_summary = (
        _summarize_prev_sections(prev_sections, prev_max)
        if prev_sections and prev_max > 0 else ""
    )

    demand_context = ""
    if demand_company:
        demand_context += f"\n\n【수요기업 정보】\n수요기업명: {demand_company}"
    if demand_text:
        demand_context += f"\n수요기업 IR자료:\n{truncate_text(demand_text, 3000)}"
    if demand_context:
        demand_context += "\n→ 위 수요기업 관점에서 기술이전 가능성과 적합성을 강조하여 작성할 것"

    biz_context = ""
    if biz_type != "자동 판단 (AI 추론)":
        biz_context = f"\n\n【사업화 유형 지시】\n{biz_type} 관점으로 작성할 것"

    extra_context = f"\n\n【추가 지시사항】\n{extra_note}" if extra_note else ""

    prev_block = ""
    if prev_summary:
        prev_block = f"""
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
【이전 섹션 핵심 내용 — 일관성 유지용 참고】
{prev_summary}
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"""

    section_prompt = SECTION_PROMPTS.get(section_name, "")

    user_message = f"""
아래 특허 명세서를 분석하여 VF TBD 보고서를 작성하라.
{biz_context}
{demand_context}
{extra_context}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
【특허 명세서 전문】
{patent_text_trimmed}
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
{prev_block}

{section_prompt}

【작성 시 반드시 지킬 사항】
1. 위 섹션 프롬프트에 명시된 모든 표의 컬럼 구조를 그대로 사용하고, 실제 내용으로 채울 것
2. "[...]" 또는 "..." 형태의 플레이스홀더를 그대로 남기지 말고 반드시 실제 내용으로 채울 것
3. 명세서에 없는 수치/사실은 "(추정)" "(확인 필요)" "(후속 조사 필요)"로 표시할 것
4. 문체: 개조식 ("~임", "~할 수 있음", "~로 판단됨")
5. 각 서브섹션을 완결하여 작성할 것 — 내용이 중간에 잘리지 않도록 할 것
6. 분량: 충분히 상세하게 (해당 섹션만으로 A4 8~12쪽 수준 목표)
"""

    max_tokens = SECTION_MAX_TOKENS.get(section_name, 16000)

    with client.messages.stream(
        model      = "claude-sonnet-4-6",
        max_tokens = max_tokens,
        system     = SYSTEM_PROMPT,
        messages   = [{"role": "user", "content": user_message}],
    ) as stream:
        for text_chunk in stream.text_stream:
            yield text_chunk
